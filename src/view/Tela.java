    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // editar
        Cliente c = new Cliente();
        c.setNome(jTable.getValueAt(jTable.getSelectedRow(), 0).toString());
        try {
            for (Cliente cliente : lista) {
                //compara os dados
                if (cliente.getNome() == c.getNome()) {
                    int i = lista.indexOf(cliente);
                    //crio um novo objeto para popular o texto digitado
                    Cliente ct = new Cliente();
                    ct.setNome(nome.getText());
                    lista.set(i, ct);
                    JOptionPane.showMessageDialog(null, "Dado atualizado com sucesso");
                }
                listar();
            }
        } catch (Exception e) {
            e.getMessage();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

